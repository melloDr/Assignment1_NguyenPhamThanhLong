package dtos;

/**
 *
 * @author Admin
 */
public class QuantityDto {
    String foodId;
    int quantity;

    public QuantityDto() {
    }

    public QuantityDto(String foodId, int quantity) {
        this.foodId = foodId;
        this.quantity = quantity;
    }

    public String getFoodId() {
        return foodId;
    }

    public void setFoodId(String foodId) {
        this.foodId = foodId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
    
}
